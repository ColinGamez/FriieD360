import fs from 'node:fs/promises';
import path from 'node:path';

const SOURCE_URL = 'https://gist.githubusercontent.com/ConzZah/5482b9e748f5de5ec49114cacc6c28f1/raw/Xbox360TitleIDs.txt';
const OUTPUT_FILE = path.resolve('src/data/titleCatalog.generated.ts');

function parseLines(text) {
  const entries = new Map();

  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.replace(/^\uFEFF/, '').trim();
    if (!line || line.startsWith('##')) {
      continue;
    }

    const delimiterIndex = line.indexOf('~');
    if (delimiterIndex === -1) {
      continue;
    }

    const id = line.slice(0, delimiterIndex).trim().toUpperCase();
    const name = line.slice(delimiterIndex + 1).trim();
    if (!/^[0-9A-F]{8}$/.test(id) || !name) {
      continue;
    }

    if (!entries.has(id)) {
      entries.set(id, { id, name });
    }
  }

  return [...entries.values()].sort((left, right) => left.id.localeCompare(right.id));
}

function buildFile(entries) {
  return `// This file is generated by scripts/update-title-db.mjs.\n// Source: ${SOURCE_URL}\n// Do not edit manually.\n\nexport interface RawTitleCatalogEntry {\n  id: string;\n  name: string;\n}\n\nexport const XBOX_360_TITLE_DB_RAW: RawTitleCatalogEntry[] = ${JSON.stringify(entries, null, 2)};\n`;
}

const response = await fetch(SOURCE_URL);
if (!response.ok) {
  throw new Error(`Failed to download title database: ${response.status} ${response.statusText}`);
}

const sourceText = await response.text();
const entries = parseLines(sourceText);

await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
await fs.writeFile(OUTPUT_FILE, buildFile(entries), 'utf8');

console.log(`Generated ${entries.length} title records at ${OUTPUT_FILE}`);
